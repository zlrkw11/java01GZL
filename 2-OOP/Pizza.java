public class Pizza {
    // variables declare
    String bread;
    String sauce;
    String cheese;
    String topping;

    // constructor 1
    Pizza(String bread){

        // assign
        this.bread = bread;

    };

    // constructor 1
    Pizza(String bread, String sauce){

        // assign
        this.bread = bread;
        this.sauce = sauce;

    };
    // constructor 2
    Pizza(String bread, String sauce, String cheese){

        // assign
        this.bread = bread;
        this.sauce = sauce;
        this.cheese = cheese;

    };

    // constructor 3
    Pizza(String bread, String sauce, String cheese, String topping){

        // assign
        this.bread = bread;
        this.sauce = sauce;
        this.cheese = cheese;
        this.topping = topping;
        
    };
}
